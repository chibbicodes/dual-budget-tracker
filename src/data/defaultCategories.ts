/**
 * Generate categories with IDs and timestamps
 */
export function generateDefaultCategories(): Category[] {
  const now = new Date().toISOString()
  const allCategories = [
    ...HOUSEHOLD_CATEGORIES,
    ...BUSINESS_EXPENSE_CATEGORIES,
    ...INCOME_CATEGORIES,
    ...TRANSFER_CATEGORIES
  ]

  return allCategories.map((cat) => ({
    ...cat,
    id: generateId(),
    createdAt: now,
    updatedAt: now,
  }))
}

